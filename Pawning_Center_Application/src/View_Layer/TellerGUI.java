    private void FinishjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinishjButton2ActionPerformed
        // TODO add your handling code here:
        try{
         name=this.NameTextField2.getText();
         nic =this.NICTextField2.getText();
         address = this.AddressTextField2.getText();
         tel_num =this.TelNumberTextField2.getText();
         email =this.EmailTextField2.getText();
        
         pawn_description=this.DescriptionTextField2.getText();
         weight =Double.parseDouble(this.WeightTextField2.getText());
         assess_value= Double.parseDouble(this.AssessValueTextField2.getText());
         allowance=Double.parseDouble(this.AllowancejTextField2.getText());
        
        //allow to get allowance less than assess value 
        if(allowance>=assess_value){
            JOptionPane.showMessageDialog(null, "Please enter value bellow assess value!");
            this.AllowancejTextField2.setText(null);
        }
       
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");     // to get init date
        DateFormat timeformat=new SimpleDateFormat("hh:mm:ss");         // to get init time
        
        Date time = new Date();
        init_time=timeformat.format(time);
        //Time start_time=init_time.
        this.InitTimeTextField2.setText(init_time.toString());
        
        Date date = new Date();
        init_date= dateFormat.parse(dateFormat.format(date)); 
        this.InitDateTextField2.setText(dateFormat.format(init_date).toString());
        
        time_period = Integer.parseInt(this.TimePeriodjComboBox2.getSelectedItem().toString());
        
        
        
        max_account_num= pawning_center_application.getMaxAccountID();
        max_pawn_num=pawning_center_application.getMaxPawnID();
        this.AccountIDTextField2.setText(Integer.toString(max_account_num+1));
        this.PawnIDTextField2.setText(Integer.toString(max_pawn_num+1));
        
        //to generate close date by current date and and the time period 
        
        close_date=pawning_center_application.getCloseDate(date, time_period);
        
        this.CloseDatejTextField2.setText(dateFormat.format(close_date).toString());
        DecimalFormat df = new DecimalFormat("#.##");
        
        double interest=pawning_center_application.getIntersetRate(time_period);
        //double init_lack=pawning_center_application.calculateInitLack(allowance,interest );
        
        //double installment = pawning_center_application.calculateInstallment(allowance, interest, time_period);
        
        init_balance=Double.parseDouble(df.format(pawning_center_application.calculateInitLack(allowance,interest )));
        installment=Double.parseDouble(df.format(pawning_center_application.calculateInstallment(allowance,interest,time_period )));
        this.InitBalancejTextField2.setText(Double.toString(init_balance));
        this.InstallmentjTextField2.setText(Double.toString(installment));
        
       
        
        
        
      
        
       
        
        }
        
        catch(Exception e){
            
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Please Insert Appropriate Data !");
        }
       
       // pawn_description.
        
    }//GEN-LAST:event_FinishjButton2ActionPerformed

