    /*Access to the Data Link Layer throgh Domain Layer in order to check user info*/
    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        String username = this.UserNameText.getText();
        String password = this.PasswordPasword.getText();
        
        Pawning_Center_Application pawning_center_application=new Pawning_Center_Application();
       
        if(pawning_center_application.createLogin(username, password)!=null){
            if(pawning_center_application.createLogin(username, password).equals("Teller")){
                this.hide();
                
                TellerGUI tellergui = new TellerGUI();
                //pawning_center_application.setUserID(username);
                User.setUserName(username);
                tellergui.show();
            }
            else if(pawning_center_application.createLogin(username, password).equals("Officer")){
                this.hide();
                OfficerGUI officergui = new OfficerGUI();
                 User.setUserName(username);
                officergui.show();
            }
            else if(pawning_center_application.createLogin(username, password).equals("Manager")){
                this.hide();
                ManagerGUI managergui= new ManagerGUI();
                User.setUserName(username);
                managergui.show();
            
            }
            else if(pawning_center_application.createLogin(username, password).equals("fail")){
                JOptionPane.showMessageDialog(null, "Loggin Failed!");
            }
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Invalid Username or Password ");
        }
        
        
    }//GEN-LAST:event_SubmitButtonActionPerformed

