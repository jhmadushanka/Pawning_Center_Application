    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String newpw=this.NewPasswordjPasswordField1.getText();
        String conpw=this.ConfirmPasswordjPasswordField1.getText();
        Pawning_Center_Application pawning_center_application=new Pawning_Center_Application();
        
        if(newpw.equals(conpw)){
            String status= pawning_center_application.change_password(UserNameText.getText(),PasswordPasword.getText(),newpw);
            JOptionPane.showMessageDialog(rootPane, status);
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Enter New Password Again !");
        
        this.NewPasswordjPasswordField1.setText("");
        this.ConfirmPasswordjPasswordField1.setText("");
        this.UserNameText.setText("");
        this.PasswordPasword.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

